#include <stdio.h>

int ft_isalpha(char c)
{
	if (c >= 'a' && c <= 'z' || c >= 'A' && c <= 'Z')
		return (1);
	return(0);
}
int main()
{
	char c = 'b';

	printf("%d\n", ft_isalpha(c));
}

